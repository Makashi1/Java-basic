package Variables;

public class Operators {
    public static void  main(String[] args) {
        int i = 5;
        int j = 4;
//        post inkrementacja
        System.out.println ( i ++);
        System.out.println ( i );
// pre dekrementacja
        System.out.println( --j );
        System.out.println( j );

    }
}
